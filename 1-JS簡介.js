/*
?[JS是什麼？]
* Web程式語言
* 最多人使用的程式語言
* 瀏覽器js直譯器(interpreters), Node.js上編寫JavaScript
* 高階, 動態, 直譯式
* 物件導性, 函式性
* 語法-Java, 一等函數-Scheme, 基於原型之繼承-Self
?[JS名稱, 版本與模式]
* 由Netscape所創造
* ECMAScript - 語言標準
* Javascript - 程式語言
* 為了維持回朔相容性, 新版本可兼容舊版本
* ES5後版本可使用嚴格模式(strict mode) - 使早期版本之錯誤被改正
?[JS執行]
* Jacascript核心定義了最小的API處理"數字, 文字, 陣列, 集合等", 但不包含IO
* IO由宿主環境(host environment)提供
*   Web瀏覽器 - 透過HTTP請求輸入, HTML和CSS輸出
*   Node(新!) - 讀取整個作業喜系統, 可讀寫檔案, 網路發送等等
* JS整體框架: 
*   JS核心 -> 宿主環境(客戶端js, 伺服端js) -> 標準程式庫, API等等
?[JS快速導覽]
* 註解(comment)//
* 變數(variable)可透過等號賦值(v?alue)
* 物件(object)是JS最重要的資料型態
* 運算式(expression) - 片語可被估算而產生一個值, 最常見的方法為使用運算子(operators)
* 述句(statements)- 句子沒有值, 但會更動狀態, 如變數宣告
*/



